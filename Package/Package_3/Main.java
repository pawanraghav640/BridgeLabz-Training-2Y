package Package.Package_3;
import static java.lang.Math.*;

public class Main {
	public static void main(String[] args) {
		System.out.println(sqrt(5));
		System.out.println(pow(5,2));
		System.out.println(max(5,7));
		System.out.println(min(5,7));
		System.out.println(abs(2.53));
//		static import can be useful because by using it we can make code more readable and we do not have to write prefix 'Math.'
	}
}
